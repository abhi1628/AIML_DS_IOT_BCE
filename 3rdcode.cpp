//Comparison Operators in CPP
#include<iostream>
using namespace std;
int main(){
	int a = 15;
	int b = 6;
	cout << "Equal " << (a == b) << endl;
	cout << "Not Equal " << (a != b) << endl;
	cout << "Greater Than " << (a > b) << endl;
	cout << "Smaller Than " << (a < b) << endl;
	cout << "Greater Than or Equal To " << (a >= b) << endl;
	cout << "Less Than or Equal To " << (a <= b) << endl;
}
