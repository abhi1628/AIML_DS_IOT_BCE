//Arithmetic Operators in CPP
#include<iostream>
using namespace std;
int main(){
	int a = 15;
	int b = 6;
	cout << "Add " << a+b << endl;
	cout << "Sub " << a-b << endl;
	cout << "Multiplication " << a*b << endl;
	cout << "Division " << a/b << endl;
	cout << "Remainder " << a%b << endl;
}
