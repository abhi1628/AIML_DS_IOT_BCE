//Arithmetic Operators in CPP using user define input
#include<iostream>
using namespace std;
int main(){
	int a, b;
	cout << "Enter 1st no.";
	cin >> a;
	cout << "Enter 2nd no.";
	cin >> b;
	cout << "Add " << a+b << endl;
	cout << "Sub " << a-b << endl;
	cout << "Multiplication " << a*b << endl;
	cout << "Division " << a/b << endl;
	cout << "Remainder " << a%b << endl;
}
